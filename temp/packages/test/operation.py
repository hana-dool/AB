def add(a: float, b: float) -> float:
    """두 값을 더해서, 그 결과를 출력

    Args:
        a (float): 더해질 값
        b (float): 더해질 값

    Returns:
        float: 두 값을 곱했을때의 결과
    """
    return a + b


def mul(a: float, b: float):
    """두 값을 곱해서 그 결과 출력

    Args:
        a (float): 곱해질 값
        b (float): 곱해질 값

    Returns:
        float : 두 값을 더했을때의 결과
    """
    return a * b
